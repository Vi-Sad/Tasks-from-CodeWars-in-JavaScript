// Преобразование шестнадцатеричного числа в десятичное

// Завершите работу функции, которая преобразует шестнадцатеричное число (заданное в виде строки) в десятичное число.

// -------------------------------------------------------------------------------------------------------------------

// Решение:

function hexToDec(hexString) {
    return parseInt(hexString, 16);
}